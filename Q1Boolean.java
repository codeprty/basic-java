public class Q1Boolean {
    public static void main(String[] args) {
        // Declare a boolean variable
        boolean my_boolean;

        // Initialize my_boolean to true
        my_boolean = true;

        // Print the value of my_boolean
        System.out.println("my_boolean = " + my_boolean);

        // Declare a boolean variable and initialize it to false
        boolean test = false;

        // Reassign my_boolean to another boolean value
        my_boolean = test;

        // Print the updated value of my_boolean
        System.out.println("my_boolean = " + my_boolean);
    }
}
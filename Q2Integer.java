public class Q2Integer {
    public static void main(String[] args) {
        // Declare integer variables
        int num1 = 2, num2 = 4, num3 = 6;
        
        // Declare an integer variable and initialize it to num1
        int my_int = num1;

        // Print the value of my_int
        System.out.println("my_int = " + my_int);

        // Reassign my_int to num2
        my_int = num2;

        // Print the updated value of my_int
        System.out.println("my_int = " + my_int);

        // Reassign my_int to num3
        my_int = num3;

        // Print the updated value of my_int
        System.out.println("my_int = " + my_int);
    }
}

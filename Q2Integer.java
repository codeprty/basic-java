public class Q2Integer {
    public static void main(String[] args) {
        // Declare an integer variable
        int my_int;

        // Initialize my_int to the first value
        my_int = 2;

        // Prints the value of my_int
        System.out.println("my_int = " + my_int);

        // Reassign my_int to the second value
        my_int = 4;

        // Prints the updated value of my_int
        System.out.println("my_int = " + my_int);

        // Reassign my_int to the third value
        my_int = 6;

        // Prints the updated value of my_int
        System.out.println("my_int = " + my_int);
    }
}
public class Q3Double {
    public static void main(String[] args) {
        // Declare a double variable
        double my_double;

        // Initialize my_double to the value of zero
        my_double = 0;

        // Prints the value of my_double
        System.out.println("my_double = " + my_double);

        // Reassign my_double to the value of one
        my_double = 1;

        // Prints the updated value of my_double
        System.out.println("my_double = " + my_double);
    }
}